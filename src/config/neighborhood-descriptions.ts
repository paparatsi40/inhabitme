/**
 * Obtiene la descripción de un barrio
 * @param citySlug - Slug de la ciudad (ej: 'madrid')
 * @param neighborhoodSlug - Slug del barrio (ej: 'malasana')
 */
export function getNeighborhoodDescription(
  citySlug: unknown,
  neighborhoodSlug: unknown,
  cityName: string,
  neighborhoodName: string
): string {
  const cityKey =
    typeof citySlug === "string" && citySlug.trim() ? citySlug.toLowerCase() : ""
  const neighborhoodKey =
    typeof neighborhoodSlug === "string" && neighborhoodSlug.trim()
      ? neighborhoodSlug.toLowerCase()
      : ""

  const description =
    cityKey && neighborhoodKey
      ? NEIGHBORHOOD_DESCRIPTIONS[cityKey]?.[neighborhoodKey]
      : undefined

  if (description) return description

  // Fallback genérico
  return `${neighborhoodName} es uno de los barrios que mejor representa la vida en ${cityName}. Ideal para estancias de 1 a 12 meses, perfecto para nómadas digitales que buscan equilibrio entre comodidad, conectividad y calidad de vida.`
}

/**
 * Obtiene la descripción corta de un barrio (para cards)
 */
export function getNeighborhoodShortDescription(
  citySlug: unknown,
  neighborhoodSlug: unknown
): string | null {
  const cityKey =
    typeof citySlug === "string" && citySlug.trim() ? citySlug.toLowerCase() : ""
  const neighborhoodKey =
    typeof neighborhoodSlug === "string" && neighborhoodSlug.trim()
      ? neighborhoodSlug.toLowerCase()
      : ""

  if (!cityKey || !neighborhoodKey) return null

  return NEIGHBORHOOD_SHORT_DESCRIPTIONS[cityKey]?.[neighborhoodKey] || null
}